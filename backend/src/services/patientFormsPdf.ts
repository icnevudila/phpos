export async function generateMedicalHistoryPdf(
  clinicId: string,
  patientId: string,
): Promise<Buffer> {
  const clinic = await fetchClinicHeader(clinicId);
  const p = await loadPatientFull(clinicId, patientId);
  const m = p.medicalRecord;

  const { doc, promise } = buildDoc();
  drawHeader(doc, clinic, "Medical History Questionnaire");

  sectionTitle(doc, "Patient");
  const fullName = [p.firstName, p.middleName, p.lastName].filter(Boolean).join(" ");
  twoColGrid(doc, [
    ["Full name", fullName],
    ["Date of birth", formatDate(p.birthDate)],
    ["Age", ageFromBirthDate(p.birthDate)],
    ["Gender", p.gender ?? ""],
    ["Phone", p.phone],
    [
      "Recorded by",
      m?.recordedBy ? `Dr. ${m.recordedBy.firstName} ${m.recordedBy.lastName}` : "—",
    ],
    ["Recorded at", m?.recordedAt ? formatDateTime(m.recordedAt) : ""],
    ["Version", m?.version ? `v${m.version}` : ""],
  ]);

  sectionTitle(doc, "General health");
  twoColGrid(doc, [
    ["Currently under physician care?", yesNo(m?.underPhysicianCare)],
    ["Reason", m?.underPhysicianCareReason ?? ""],
    ["Ever hospitalized?", yesNo(m?.hospitalized)],
    [
      "Hospitalization details",
      [m?.hospitalizedYear ? String(m.hospitalizedYear) : null, m?.hospitalizedReason ?? null]
        .filter(Boolean)
        .join(" — "),
    ],
    ["Taking medications?", yesNo(m?.takingMedications)],
    ["Medications", m?.medicationsList ?? ""],
    ["Serious illness / surgery?", yesNo(m?.seriousIllness)],
    ["Details", m?.seriousIllnessDetails ?? ""],
  ]);

  // 28 koşul — kontrol listesi
  sectionTitle(doc, "Medical conditions");
  const conditions = new Set<string>(m?.conditions ?? []);
  const listColWidth = 249;
  const itemH = 14;
  let y = doc.y;
  const startY = y;
  const codes = MEDICAL_CONDITION_CODES;
  for (let i = 0; i < codes.length; i++) {
    const code = codes[i];
    const col = i % 2;
    const row = Math.floor(i / 2);
    const x = 48 + col * listColWidth;
    const cy = startY + row * itemH;
    if (cy > 760) continue;
    doc.strokeColor("#0f172a").lineWidth(0.7).rect(x, cy, 8, 8).stroke();
    if (conditions.has(code)) {
      doc
        .save()
        .strokeColor("#10b981")
        .lineWidth(1.2)
        .moveTo(x + 1, cy + 4)
        .lineTo(x + 3, cy + 6)
        .lineTo(x + 7, cy + 1)
        .stroke()
        .restore();
    }
    doc.fillColor("#0f172a").fontSize(9).text(CONDITION_LABEL[code], x + 12, cy - 1, {
      width: listColWidth - 20,
    });
  }
  const rows = Math.ceil(codes.length / 2);
  doc.y = startY + rows * itemH + 8;

  if (m?.conditionsOther) {
    doc.fontSize(9).fillColor("#64748b").text("Other / notes", 48, doc.y);
    doc.fontSize(10).fillColor("#0f172a").text(m.conditionsOther, 48, doc.y + 10, { width: 499 });
    doc.moveDown(1);
  }

  sectionTitle(doc, "Allergies");
  twoColGrid(doc, [
    ["Local anesthetic", yesNo(m?.allergyAnesthetic)],
    ["Penicillin", yesNo(m?.allergyPenicillin)],
    ["Sulfa drugs", yesNo(m?.allergySulfa)],
    ["Aspirin", yesNo(m?.allergyAspirin)],
    ["Latex / Rubber", yesNo(m?.allergyLatex)],
    ["Other", m?.allergyOther ?? ""],
  ]);

  sectionTitle(doc, "Lifestyle");
  twoColGrid(doc, [
    ["Smoking", m?.smoker ?? ""],
    ["Alcohol", m?.alcohol ?? ""],
    ["Recreational drug use", m?.recreationalDrug ?? ""],
  ]);

  if (p.gender === "FEMALE") {
    sectionTitle(doc, "Female-specific");
    twoColGrid(doc, [
      ["Pregnant?", yesNo(m?.isPregnant)],
      ["Pregnancy month", m?.pregnancyMonths ? String(m.pregnancyMonths) : ""],
      ["Breastfeeding?", yesNo(m?.isBreastfeeding)],
      ["Using contraceptives?", yesNo(m?.usesContraceptive)],
    ]);
  }

  if (m?.notes) {
    sectionTitle(doc, "Additional notes");
    doc.fontSize(10).fillColor("#0f172a").text(m.notes, { width: 499 });
  }

  signatureBlock(doc, "Patient / Guardian signature", "Dentist signature");
  drawFooter(doc);
  doc.end();
  return promise;
}

