type Doc = PDFKit.PDFDocument;

interface ClinicHeaderData {
  name: string;
  address: string | null;
  city: string | null;
  phone: string | null;
}

async function fetchClinicHeader(clinicId: string): Promise<ClinicHeaderData> {
  const c = await prisma.clinic.findUnique({
    where: { id: clinicId },
    select: { name: true, address: true, city: true, phone: true },
  });
  return {
    name: c?.name ?? "DentEase PH",
    address: c?.address ?? null,
    city: c?.city ?? null,
    phone: c?.phone ?? null,
  };
}

function formatDate(d: Date | null | undefined): string {
  if (!d) return "—";
  return new Date(d).toLocaleDateString("en-PH", {
    timeZone: "Asia/Manila",
    year: "numeric",
    month: "short",
    day: "2-digit",
  });
}

function formatDateTime(d: Date | null | undefined): string {
  if (!d) return "—";
  return new Date(d).toLocaleString("en-PH", {
    timeZone: "Asia/Manila",
  });
}

function money(v: string | number | null | undefined): string {
  if (v === null || v === undefined || v === "") return "₱ 0.00";
  const num = typeof v === "string" ? Number(v) : v;
  return `₱ ${num.toLocaleString("en-PH", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

function ageFromBirthDate(d: Date | null | undefined): string {
  if (!d) return "—";
  const now = new Date();
  const birth = new Date(d);
  let age = now.getFullYear() - birth.getFullYear();
  const mDiff = now.getMonth() - birth.getMonth();
  if (mDiff < 0 || (mDiff === 0 && now.getDate() < birth.getDate())) age--;
  return String(age);
}

function yesNo(v: boolean | null | undefined): string {
  if (v === true) return "Yes";
  if (v === false) return "No";
  return "—";
}

function drawHeader(doc: Doc, clinic: ClinicHeaderData, title: string): void {
  doc.fillColor("#0f172a").fontSize(18).text(clinic.name, 48, 48);
  doc
    .fillColor("#64748b")
    .fontSize(9)
    .text([clinic.address, clinic.city].filter(Boolean).join(", "), 48, 72);
  if (clinic.phone) doc.text(`Tel: ${clinic.phone}`, 48, 84);

  doc
    .fillColor("#10b981")
    .fontSize(13)
    .text(title, 48, 106, { align: "right", width: 499 });

  doc
    .strokeColor("#10b981")
    .lineWidth(2)
    .moveTo(48, 128)
    .lineTo(547, 128)
    .stroke();

  doc.y = 140;
  doc.x = 48;
}

function drawFooter(doc: Doc, note?: string): void {
  const m = doc.page.margins;
  const bottomY = doc.page.height - m.bottom;
  doc
    .strokeColor("#cbd5e1")
    .lineWidth(0.5)
    .moveTo(48, bottomY - 14)
    .lineTo(547, bottomY - 14)
    .stroke();
  doc
    .fontSize(8)
    .fillColor("#94a3b8")
    .text(
      note ?? "Generated by DentEase PH — Internal clinic record.",
      48,
      bottomY - 10,
      { align: "center", width: 499 },
    );
}

function sectionTitle(doc: Doc, title: string): void {
  if (doc.y > 740) doc.addPage();
  doc.moveDown(0.6);
  doc
    .fillColor("#0f172a")
    .fontSize(12)
    .text(title, { underline: false });
  doc
    .strokeColor("#10b981")
    .lineWidth(1)
    .moveTo(doc.x, doc.y + 1)
    .lineTo(doc.x + 499, doc.y + 1)
    .stroke();
  doc.moveDown(0.3);
  doc.fillColor("#0f172a").fontSize(10);
}

/**
 * Basit iki-sütunlu "label: value" grid.
 * rows: her eleman [label, value]
 */
function twoColGrid(doc: Doc, rows: Array<[string, string]>): void {
  const colW = 247;
  const rowH = 16;
  const startX = doc.x;
  let y = doc.y;
  for (let i = 0; i < rows.length; i += 2) {
    if (y > 770) {
      doc.addPage();
      y = doc.y;
    }
    const left = rows[i];
    const right = rows[i + 1];
    doc.fontSize(9).fillColor("#64748b").text(left[0], startX, y);
    doc.fontSize(10).fillColor("#0f172a").text(left[1] || "—", startX, y + 10, { width: colW - 10 });
    if (right) {
      doc.fontSize(9).fillColor("#64748b").text(right[0], startX + colW, y);
      doc
        .fontSize(10)
        .fillColor("#0f172a")
        .text(right[1] || "—", startX + colW, y + 10, { width: colW - 10 });
    }
    y += rowH + 10;
  }
  doc.y = y + 4;
  doc.x = startX;
}

function signatureBlock(doc: Doc, labelLeft: string, labelRight: string): void {
  if (doc.y > 700) doc.addPage();
  doc.moveDown(2);
  const y = doc.y;
  doc.strokeColor("#0f172a").lineWidth(0.7);
  doc.moveTo(48, y).lineTo(280, y).stroke();
  doc.moveTo(320, y).lineTo(547, y).stroke();
  doc.fillColor("#475569").fontSize(9);
  doc.text(labelLeft, 48, y + 4, { width: 232, align: "center" });
  doc.text(labelRight, 320, y + 4, { width: 227, align: "center" });
  doc.moveDown(2);
  doc.x = 48;
}

function buildDoc(): { doc: Doc; chunks: Buffer[]; promise: Promise<Buffer> } {
  const doc = new PDFDocument({ size: "A4", margin: 48, bufferPages: true });
  applyPdfUnicodeFont(doc);
  const chunks: Buffer[] = [];
  doc.on("data", (c: Buffer) => chunks.push(c));
  const promise = new Promise<Buffer>((resolve, reject) => {
    doc.on("end", () => resolve(Buffer.concat(chunks)));
    doc.on("error", reject);
  });
  return { doc, chunks, promise };
}

async function loadPatientFull(clinicId: string, patientId: string) {
  const patient = await prisma.patient.findFirst({
    where: { id: patientId, clinicId },
    include: {
      teeth: { orderBy: { toothNumber: "asc" } },
      medicalRecord: {
        include: { recordedBy: { select: { firstName: true, lastName: true } } },
      },
      appointments: {
        orderBy: { scheduledAt: "desc" },
        take: 10,
        include: { dentist: { select: { firstName: true, lastName: true } } },
      },
      invoices: {
        orderBy: { createdAt: "desc" },
        take: 50,
        include: {
          payments: true,
          appointment: {
            include: {
              treatments: {
                include: { dentist: { select: { firstName: true, lastName: true } } },
              },
            },
          },
        },
      },
    },
  });
  if (!patient) {
    throw new Error("Patient not found");
  }
  return patient;
}

