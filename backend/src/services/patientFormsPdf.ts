export async function generateDentalRecordPdf(
  clinicId: string,
  patientId: string,
): Promise<Buffer> {
  const clinic = await fetchClinicHeader(clinicId);
  const p = await loadPatientFull(clinicId, patientId);

  const { doc, promise } = buildDoc();
  drawHeader(doc, clinic, "Dental Record");

  sectionTitle(doc, "Patient information");
  const fullName = [p.firstName, p.middleName, p.lastName].filter(Boolean).join(" ");
  twoColGrid(doc, [
    ["Full name", fullName],
    ["Nickname", p.nickname ?? ""],
    ["Date of birth", formatDate(p.birthDate)],
    ["Age", ageFromBirthDate(p.birthDate)],
    ["Gender", p.gender ?? ""],
    ["Civil status", p.civilStatus ?? ""],
    ["Phone", p.phone],
    ["Email", p.email ?? ""],
    ["Occupation", p.occupation ?? ""],
    ["Nationality", p.nationality ?? ""],
    ["Religion", p.religion ?? ""],
    ["PhilHealth #", p.philhealthNo ?? ""],
    ["Address", [p.address, p.city, p.province].filter(Boolean).join(", ")],
    ["Emergency contact", [p.emergencyContactName, p.emergencyContactPhone].filter(Boolean).join(" · ")],
    ["Referred by", p.referralSource ?? ""],
    ["Previous dentist", p.previousDentist ?? ""],
    ["Last dental visit", formatDate(p.lastDentalVisit)],
    ["Reason for visit", p.reasonForVisit ?? ""],
  ]);

  sectionTitle(doc, "Vital signs & allergies");
  const bp =
    p.bloodPressureSystolic && p.bloodPressureDiastolic
      ? `${p.bloodPressureSystolic}/${p.bloodPressureDiastolic} mmHg`
      : "";
  twoColGrid(doc, [
    ["Blood pressure", bp],
    ["Pulse rate", p.pulseRate ? `${p.pulseRate} bpm` : ""],
    ["Blood type", p.bloodType ?? ""],
    ["Known allergies", (p.allergies ?? []).join(", ")],
  ]);

  // Diş durumu — tabelka: sadece default dışındaki dişler
  sectionTitle(doc, "Tooth conditions");
  const nonDefault = p.teeth.filter((t) => t.condition !== "HEALTHY" || (t.notes && t.notes.trim().length));
  if (nonDefault.length === 0) {
    doc.fontSize(10).fillColor("#64748b").text("No recorded tooth conditions.");
  } else {
    doc.fontSize(10).fillColor("#0f172a");
    const colX = { n: 48, c: 110, s: 280, u: 420 };
    const toothHdrY = doc.y;
    doc.fillColor("#64748b").fontSize(9);
    doc.text("TOOTH", colX.n, toothHdrY);
    doc.text("CONDITION", colX.c, toothHdrY);
    doc.text("SURFACES", colX.s, toothHdrY);
    doc.text("UPDATED", colX.u, toothHdrY);
    doc
      .strokeColor("#cbd5e1")
      .moveTo(48, toothHdrY + 12)
      .lineTo(547, toothHdrY + 12)
      .stroke();
    doc.y = toothHdrY + 16;
    for (const t of nonDefault) {
      if (doc.y > 770) doc.addPage();
      const y = doc.y;
      doc.fillColor("#0f172a").fontSize(10);
      doc.text(String(t.toothNumber), colX.n, y, { width: 50 });
      doc.text(t.condition.replace(/_/g, " "), colX.c, y, { width: 160 });
      doc.text((t.surfaces ?? []).join(", ") || "—", colX.s, y, { width: 130 });
      doc.text(formatDate(t.updatedAt), colX.u, y, { width: 120 });
      doc.moveDown(0.4);
    }
  }

  // Son randevular
  sectionTitle(doc, "Recent appointments");
  if (p.appointments.length === 0) {
    doc.fontSize(10).fillColor("#64748b").text("No past appointments.");
  } else {
    for (const a of p.appointments.slice(0, 8)) {
      if (doc.y > 770) doc.addPage();
      doc
        .fontSize(10)
        .fillColor("#0f172a")
        .text(
          `• ${formatDateTime(a.scheduledAt)} — ${(a.type ?? "—").replace(/_/g, " ")} — Dr. ${a.dentist.firstName} ${a.dentist.lastName} — ${a.status}`,
        );
    }
  }

  drawFooter(doc);
  doc.end();
  return promise;
}

