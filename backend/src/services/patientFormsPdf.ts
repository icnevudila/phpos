export async function generateTreatmentRecordPdf(
  clinicId: string,
  patientId: string,
): Promise<Buffer> {
  const clinic = await fetchClinicHeader(clinicId);
  const p = await loadPatientFull(clinicId, patientId);

  const { doc, promise } = buildDoc();
  drawHeader(doc, clinic, "Treatment Record");

  sectionTitle(doc, "Patient");
  const fullName = [p.firstName, p.middleName, p.lastName].filter(Boolean).join(" ");
  twoColGrid(doc, [
    ["Full name", fullName],
    ["Date of birth", formatDate(p.birthDate)],
    ["Phone", p.phone],
    ["PhilHealth #", p.philhealthNo ?? ""],
  ]);

  // Tüm tedavileri doğrudan Treatment tablosundan çek
  const treatments = await prisma.treatment.findMany({
    where: { patientId },
    orderBy: { createdAt: "asc" },
    include: { dentist: { select: { firstName: true, lastName: true } } },
  });

  type Row = {
    date: Date;
    dentist: string;
    procedure: string;
    teeth: string;
    qty: number;
    unit: string;
    total: string;
  };
  const rows: Row[] = treatments.map((t) => ({
    date: t.createdAt,
    dentist: `Dr. ${t.dentist.firstName} ${t.dentist.lastName}`,
    procedure: t.procedure.replace(/_/g, " "),
    teeth: (t.toothIds ?? []).join(", ") || "—",
    qty: t.quantity,
    unit: t.unitPrice.toString(),
    total: (Number(t.unitPrice) * t.quantity).toFixed(2),
  }));

  let totalBilled = 0;
  let totalPaid = 0;
  for (const inv of p.invoices) {
    totalBilled += Number(inv.total);
    for (const pay of inv.payments) totalPaid += Number(pay.amount);
  }

  sectionTitle(doc, "Treatments performed");
  if (rows.length === 0) {
    doc.fontSize(10).fillColor("#64748b").text("No treatments recorded yet.");
  } else {
    const col = { date: 48, proc: 120, tooth: 245, qty: 300, unit: 340, total: 410, dent: 475 };
    const tHdrY = doc.y;
    doc.fontSize(9).fillColor("#64748b");
    doc.text("DATE", col.date, tHdrY);
    doc.text("PROCEDURE", col.proc, tHdrY);
    doc.text("TOOTH", col.tooth, tHdrY);
    doc.text("QTY", col.qty, tHdrY);
    doc.text("UNIT", col.unit, tHdrY);
    doc.text("TOTAL", col.total, tHdrY);
    doc.text("DENTIST", col.dent, tHdrY);
    doc
      .strokeColor("#cbd5e1")
      .moveTo(48, tHdrY + 12)
      .lineTo(547, tHdrY + 12)
      .stroke();
    doc.y = tHdrY + 16;

    for (const r of rows) {
      if (doc.y > 770) doc.addPage();
      const rowTop = doc.y;
      const sx = doc.x;
      const sy = doc.y;
      doc.x = col.proc;
      doc.y = rowTop;
      const procH = doc.heightOfString(r.procedure, { width: 120 });
      doc.x = sx;
      doc.y = sy;
      doc.fontSize(9).fillColor("#0f172a");
      doc.text(formatDate(r.date), col.date, rowTop, { width: 70 });
      doc.text(r.procedure, col.proc, rowTop, { width: 120 });
      doc.text(r.teeth, col.tooth, rowTop, { width: 50 });
      doc.text(String(r.qty), col.qty, rowTop, { width: 35 });
      doc.text(money(r.unit), col.unit, rowTop, { width: 65 });
      doc.text(money(r.total), col.total, rowTop, { width: 60 });
      doc.text(r.dentist, col.dent, rowTop, { width: 72 });
      doc.y = rowTop + Math.max(procH, 11) + 6;
    }
  }

  sectionTitle(doc, "Financial summary");
  const paidInvoices = p.invoices.filter((i) => i.status === "PAID").length;
  const partialInvoices = p.invoices.filter((i) => i.status === "PARTIAL").length;
  const unpaidInvoices = p.invoices.filter((i) => i.status === "UNPAID").length;
  twoColGrid(doc, [
    ["Total billed", money(totalBilled)],
    ["Total paid", money(totalPaid)],
    ["Outstanding balance", money(totalBilled - totalPaid)],
    ["Invoices count", String(p.invoices.length)],
    ["Paid invoices", String(paidInvoices)],
    ["Partially paid invoices", String(partialInvoices)],
    ["Unpaid invoices", String(unpaidInvoices)],
    ["Last payment date", formatDate(p.invoices.flatMap((i) => i.payments).sort((a, b) => b.paidAt.getTime() - a.paidAt.getTime())[0]?.paidAt)],
  ]);

  drawFooter(doc);
  doc.end();
  return promise;
}

